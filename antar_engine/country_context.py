def get_country_context(country_code: str) -> str:
    """Return a descriptive string about cultural norms for the country."""
    if country_code not in COUNTRY_CONTEXT:
        return ""
    ctx = COUNTRY_CONTEXT[country_code]["cultural"]
    parts = [
        f"Marriage norms: {ctx['marriage']}.",
        f"Career landscape: {ctx['career']}.",
        f"General culture: {ctx['culture']}.",
    ]
    if "economic" in ctx:
        parts.append(f"Economic context: {ctx['economic']}.")
    return (f"Cultural context ({country_code}): " + " ".join(parts))

def get_nation_chart_params(country_code: str) -> dict:
    """Return the nation chart birth parameters for the given country code."""
    if country_code not in COUNTRY_CONTEXT:
        return None
    return COUNTRY_CONTEXT[country_code].get("nation_chart")
