def get_annual_house(natal_house: int, age_at_last_birthday: int) -> int:
    """
    Return the annual (Varshphal) house for a planet in natal_house,
    for a person who has completed age_at_last_birthday years.
    
    running_year = age_at_last_birthday + 1
    Clamped to 1–120 (the table covers birth to age 119).
    """
    running_year = age_at_last_birthday + 1
    running_year = max(1, min(120, running_year))
    return VARSHAPHAL_TABLE[running_year][natal_house - 1]
